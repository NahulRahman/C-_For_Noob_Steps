#include<iostream>
using namespace std;
int main(){
//increment and decrement operators are included
//pre increment ++X  first increment then print
//post increment X++ first print then increment
   cout<<"A++ = "<<a++<<endl;
    cout<<"++A = "<<++a<<endl;
    cout<<"A-- = "<<a--<<endl;
    cout<<"--A = "<<--a<<endl;
    cout<<"B++ = "<<b++<<endl;
    cout<<"++B = "<<++b<<endl;
    cout<<"B-- = "<<b--<<endl;
    cout<<"--B = "<<--b<<endl;
    return 0;
}
