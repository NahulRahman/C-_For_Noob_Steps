#include<iostream>
using namespace std;
int main(){
    int a,b;
    cin>>a>>b;
    cout<<"A   = "<<a<<endl;
    cout<<"B   = "<<b<<endl;
    cout<<"A+B = "<<a+b<<endl;
    cout<<"A-B = "<<a-b<<endl;
    cout<<"A*B = "<<a*b<<endl;
    cout<<"A/B = "<<a/b<<endl;
    cout<<"A%B = "<<a%b<<endl;
    //increment and decrement operators are included
    cout<<"A++ = "<<a++<<endl;
    cout<<"++A = "<<++a<<endl;
    cout<<"A-- = "<<a--<<endl;
    cout<<"--A = "<<--a<<endl;
    cout<<"B++ = "<<b++<<endl;
    cout<<"++B = "<<++b<<endl;
    cout<<"B-- = "<<b--<<endl;
    cout<<"--B = "<<--b<<endl;
    return 0;
}
