#include<iostream>
using namespace std;
int main(){
    int a,b; // let a=1, b=2
    cin>>a>>b;
    cout<<(a==b)<<endl; //1==2 false  0
    cout<<(a!=b)<<endl;// 1!=2 true   1
    cout<<(a>b)<<endl; // 1>2 false   0
    cout<<(a<b)<<endl; // 1<2  true   1
    cout<<(a<=b)<<endl;// 1<=2 true   1
    cout<<(a>=b)<<endl;// 1>=2 false  0
    return 0;

}

